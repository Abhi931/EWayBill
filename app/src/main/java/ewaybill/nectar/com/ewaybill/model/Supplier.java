public class Supplier {
    private String idTrans;
    private String transporterId;
    private String transporterAddress;
    private String transporterName;
    private String transporterMobileno;
    private String transporterEmail;
    private String transporterPlace;
    private String transporterPincode;
    private String transporterState;


    public String getIdTrans() {
        return idTrans;
    }

    public void setIdTrans(String idTrans) {
        this.idTrans = idTrans;
    }

    public String getTransporterId() {
        return transporterId;
    }

    public void setTransporterId(String transporterId) {
        this.transporterId = transporterId;
    }

    public String getTransporterAddress() {
        return transporterAddress;
    }

    public void setTransporterAddress(String transporterAddress) {
        this.transporterAddress = transporterAddress;
    }

    public String getTransporterName() {
        return transporterName;
    }

    public void setTransporterName(String transporterName) {
        this.transporterName = transporterName;
    }

    public String getTransporterMobileno() {
        return transporterMobileno;
    }

    public void setTransporterMobileno(String transporterMobileno) {
        this.transporterMobileno = transporterMobileno;
    }

    public String getTransporterEmail() {
        return transporterEmail;
    }

    public void setTransporterEmail(String transporterEmail) {
        this.transporterEmail = transporterEmail;
    }

    public String getTransporterPlace() {
        return transporterPlace;
    }

    public void setTransporterPlace(String transporterPlace) {
        this.transporterPlace = transporterPlace;
    }

    public String getTransporterPincode() {
        return transporterPincode;
    }

    public void setTransporterPincode(String transporterPincode) {
        this.transporterPincode = transporterPincode;
    }

    public String getTransporterState() {
        return transporterState;
    }

    public void setTransporterState(String transporterState) {
        this.transporterState = transporterState;
    }
}
